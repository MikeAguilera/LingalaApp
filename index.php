                    <h2>Introduction</h2>
                    <p>
                    Lingala is a lingua franca or trade language spoken in the areas on both sides of the Congo River from Kinshasa up to about a hundred 
                    miles from Kisangani. Lingala, usually called Mangala by Africans, was originally the language of the Bamangala, a Bantu tribe that has
                    almost completely disappeared. Most speakers of Lingala are native speakers of another language and use Lingala as a means of communicating 
                    with other tribal groups and to a lesser extent with Europeans; however, there is a growing number of younger people, especially in urban 
                    centers such as Kinshasa, whose native language is Lingala. Slnce Lingala is spoken by so many people of varied linguistic backgrounds, 
                    it ii invevitable that the language as spoken in different areas should vary to a greater or lesser degree.
                    </p>
                    <p>
                    The speaker on whose speech these materials are based is from Kinshasa, a child of parents who could not speak each other's language and 
                    who consequently always spoke Lingala with each other and with their children. Lingala has been a written language for quite some time, 
                    but unfortunately written or 'literary' Lingala is different in many ways from Lingala as it is nearly always spoken by Africans. One can 
                    say without hesitat1on that literary Lingala is an invention of Europeans who have tried to 'improve' on the language, because Africans 
                    who speak Lingala fluently have trouble understanding literary Lingala even when it is spoken. This guide is based strictly on Lingala as 
                    spoken by the average Congolese.
                    </p>
                    <p>
                    The transcription system used in this website is the orthography regularly used to write Lingala plus diacritic markings to indicate tone, 
                    Which is not usually written, and to make it clear how a word or segment is pronounced in cases where the student may have difficulty 
                    interpreting the orthograhy. <!--Some words have been respelled, but this is indicated in the footnotes the first
