                    </p>
                    <p>
                    Lingala contains many words borrowed both from African and European languages. It is often difficult, if not impossible, to recognize words borrowed from
                    other Bantu languages. Since most of the people who speak Lingala are native speakers of some other language, words from other languages are constantly 
                    used in Lingala.
                    </p>
                    <p>
                    A number of Arablc words have come in via Swahili. Portuguese and French words have been borrowed for several centuries, and many such words have been 
                    so fully assimilated into the sound system that they too are very hard to recognize. A number of English words also have entered. French words are freely
                    used in everyday speech in a more or less unassimilated form. Those persons who speak French fluently also try to pronounce French words as they would be 
                    in French when these words are used in Lingala. Assimilated words occuring in this text are spelled as pronounced in Lingala, and the first time they occur, 
                    the standard French spelling is given in parentheses, e.g., /leta/ (l'etat), 'government'. Words recently borrowed and not assimilated into the Lingala sound
                    system are given the standard French spelling in the Lingala text. Unassimilated French tones usually have a high tone on the final syllable. The diacritic 
                    marks on French words are NOT tone marks, but they are the same as those regularly used in French spelling.
                    </p>
                    <p>
                    Lingala, like almost all the languages spoken south of the Sahara, is a tone language. Each syllable has its own tone. Tone
                    is as much an integral part of a syllable as vowels and consonants. Words are distinguished by tone, e.g., /ng&aacute;&aacute;/, 'I', 'me' (a low
                    and two highs), and /ngai/, 'scur(ness)', 'bitter(ness)' (three lows). Lingala has two contrastive or phonemic tones: high / ' /, low / &nbsp; / (unmarked). 
                    However, ther'e are many speakers of Lingala who use a stress system instead of tones. Most speakers who use a stress system stress the penultimate syllable. 
                    Because of these speakers and because tone has a smaller phonemlc Yield, i.e., plays a smaller role, in Lingala than in the great majority of
                    African languages, one can obviously communicate in Lingala without using it's tone system. But the majority of speakers of the language do use tone, 
                    including the speaker used as the model for this guide. 
                    </p>
